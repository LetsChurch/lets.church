describe('getVariants', () => {
  test('standard resolutions', () => {
    expect(getVariants(3840, 2160)).toMatchInlineSnapshot(`
      [
        "VIDEO_4K",
        "VIDEO_4K_DOWNLOAD",
        "VIDEO_1080P",
        "VIDEO_1080P_DOWNLOAD",
        "VIDEO_720P",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(1920, 1080)).toMatchInlineSnapshot(`
      [
        "VIDEO_1080P",
        "VIDEO_1080P_DOWNLOAD",
        "VIDEO_720P",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(1280, 720)).toMatchInlineSnapshot(`
      [
        "VIDEO_720P",
        "VIDEO_720P_DOWNLOAD",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(842, 480)).toMatchInlineSnapshot(`
      [
        "VIDEO_480P",
        "VIDEO_480P_DOWNLOAD",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(640, 360)).toMatchInlineSnapshot(`
      [
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
  });

  test('non-standard resolutions', () => {
    expect(getVariants(4000, 4000)).toMatchInlineSnapshot(`
      [
        "VIDEO_4K",
        "VIDEO_4K_DOWNLOAD",
        "VIDEO_1080P",
        "VIDEO_1080P_DOWNLOAD",
        "VIDEO_720P",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(2000, 2000)).toMatchInlineSnapshot(`
      [
        "VIDEO_1080P",
        "VIDEO_1080P_DOWNLOAD",
        "VIDEO_720P",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(1000, 1000)).toMatchInlineSnapshot(`
      [
        "VIDEO_720P",
        "VIDEO_720P_DOWNLOAD",
        "VIDEO_480P",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
    expect(getVariants(500, 500)).toMatchInlineSnapshot(`
      [
        "VIDEO_480P",
        "VIDEO_480P_DOWNLOAD",
        "VIDEO_360P",
        "AUDIO",
        "AUDIO_DOWNLOAD",
      ]
    `);
  });
});

test('variantsToMasterVideoPlaylist', () => {
  expect(variantsToMasterVideoPlaylist(['VIDEO_4K', 'VIDEO_1080P']))
    .toMatchInlineSnapshot(`
      "#EXTM3U
      #EXT-X-VERSION:3
      #EXT-X-STREAM-INF:BANDWIDTH=18200000,RESOLUTION=3840x2160
      VIDEO_4K.m3u8
      #EXT-X-STREAM-INF:BANDWIDTH=5000000,RESOLUTION=1920x1080
      VIDEO_1080P.m3u8"
    `);

  expect(
    variantsToMasterVideoPlaylist([
      'VIDEO_1080P',
      'VIDEO_720P',
      'VIDEO_480P',
      'VIDEO_360P',
    ]),
  ).toMatchInlineSnapshot(`
    "#EXTM3U
    #EXT-X-VERSION:3
    #EXT-X-STREAM-INF:BANDWIDTH=5000000,RESOLUTION=1920x1080
    VIDEO_1080P.m3u8
    #EXT-X-STREAM-INF:BANDWIDTH=2800000,RESOLUTION=1280x720
    VIDEO_720P.m3u8
    #EXT-X-STREAM-INF:BANDWIDTH=1400000,RESOLUTION=842x480
    VIDEO_480P.m3u8
    #EXT-X-STREAM-INF:BANDWIDTH=800000,RESOLUTION=640x360
    VIDEO_360P.m3u8"
  `);
});

